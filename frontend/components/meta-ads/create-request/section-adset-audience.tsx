// "Other selected locations" gộp 2 nhóm:
// - UNIVERSAL (TH, BR): bắt buộc theo quốc gia, luôn tính.
// - FINSERV (AU, IN): chỉ tính khi người dùng tick "includes financial products".
const otherUniversalCountryCodes = new Set<RegulatedCountryKey>(["TH", "BR"])
const otherFinservCountryCodes = new Set<RegulatedCountryKey>(["AU", "IN"])

function getOtherRegulatedCountryCodes(includesFinancialProducts: boolean): RegulatedCountryKey[] {
  return includesFinancialProducts
    ? [...otherUniversalCountryCodes, ...otherFinservCountryCodes]
    : [...otherUniversalCountryCodes]
}

// Có nước FINSERV (AU/IN) trong target không — dùng để quyết định hiển thị checkbox tài chính.
function hasFinservTargetCountry(form: RequestFormState, countryGroups: GeoCountryGroupDto[]): boolean {
  const excluded = new Set(normalizeCountryCodes(form.excludedCountries))
  if (form.geoMode === "GLOBAL") {
    return [...otherFinservCountryCodes].some((country) => !excluded.has(country))
  }
  if (form.geoMode === "REGION") {
    return form.regionKeys.length > 0
  }

  const countryCodes = (() => {
    if (form.geoMode === "COUNTRY") return normalizeCountryCodes(form.countries)
    if (form.geoMode === "COUNTRY_GROUP") {
      const selected = new Set(form.countryGroupIds)
      return countryGroups.filter((group) => selected.has(group.id)).flatMap((group) => normalizeCountryCodes(group.countryCodes))
    }
    if (form.geoMode === "CITY") return normalizeCountryCodes(form.cityTargets.map((city) => city.countryCode ?? ""))
    return []
  })()

  const targeted = new Set(countryCodes.filter((country) => !excluded.has(country)))
  return [...otherFinservCountryCodes].some((country) => targeted.has(country))
}

function normalizeCountryCodes(values?: string[] | null) {
  return (values ?? [])
    .map((value) => value.trim().toUpperCase())
    .filter((value) => value.length === 2)
}

function getAdTransparencyCategories(form: RequestFormState, countryGroups: GeoCountryGroupDto[]): typeof transparencyCategories {
  const excluded = new Set(normalizeCountryCodes(form.excludedCountries))
  const otherCountries = getOtherRegulatedCountryCodes(form.includesFinancialProducts)
  if (form.geoMode === "GLOBAL") {
    return transparencyCategories.filter((category) => {
      if (category.key === "OTHER") return otherCountries.some((country) => !excluded.has(country))
      return !excluded.has(category.key)
    })
  }

  if (form.geoMode === "REGION") {
    return form.regionKeys.length > 0 ? transparencyCategories : []
  }

  const countryCodes = (() => {
    if (form.geoMode === "COUNTRY") return normalizeCountryCodes(form.countries)
    if (form.geoMode === "COUNTRY_GROUP") {
      const selected = new Set(form.countryGroupIds)
      return countryGroups.filter((group) => selected.has(group.id)).flatMap((group) => normalizeCountryCodes(group.countryCodes))
    }
    if (form.geoMode === "CITY") return normalizeCountryCodes(form.cityTargets.map((city) => city.countryCode ?? ""))
    return []
  })()

  const targeted = new Set(countryCodes.filter((country) => !excluded.has(country)))
  return transparencyCategories.filter((category) => {
    if (category.key === "OTHER") return otherCountries.some((country) => targeted.has(country))
    return targeted.has(category.key)
  })
}

function getRequiredAdTransparencyCategoryKeys(form: RequestFormState): Set<TransparencyCategoryKey> {
  if (form.geoMode !== "COUNTRY") return new Set()

  const excluded = new Set(normalizeCountryCodes(form.excludedCountries))
  const targeted = new Set(normalizeCountryCodes(form.countries).filter((country) => !excluded.has(country)))
  const otherCountries = getOtherRegulatedCountryCodes(form.includesFinancialProducts)
  const required = new Set<TransparencyCategoryKey>()
  if (targeted.has("SG")) required.add("SG")
  if (targeted.has("TW")) required.add("TW")
  if (otherCountries.some((country) => targeted.has(country))) required.add("OTHER")
  return required
}

function targetsEuDsa(form: RequestFormState, countryGroups: GeoCountryGroupDto[]): boolean {
  const excluded = new Set(normalizeCountryCodes(form.excludedCountries))
  if (form.geoMode === "GLOBAL") {
    return [...euCountryCodes].some((country) => !excluded.has(country))
  }
  if (form.geoMode === "REGION") {
    return form.regionKeys.length > 0
  }

  const countryCodes = (() => {
    if (form.geoMode === "COUNTRY") return normalizeCountryCodes(form.countries)
    if (form.geoMode === "COUNTRY_GROUP") {
      const selected = new Set(form.countryGroupIds)
      return countryGroups.filter((group) => selected.has(group.id)).flatMap((group) => normalizeCountryCodes(group.countryCodes))
    }
    if (form.geoMode === "CITY") return normalizeCountryCodes(form.cityTargets.map((city) => city.countryCode ?? ""))
    return []
  })()

  return countryCodes.some((country) => euCountryCodes.has(country) && !excluded.has(country))
}

