function MediaSourceEditor({
  title,
  kind,
  selection,
  uploading,
  allowExternalUrl,
  optional,
  adAccountId,
  onModeChange,
  onPatch,
  onUpload,
  onMetaSelect,
}: {
  title: string
  kind: "image" | "video"
  selection: MetaRequestAssetSelectionState
  uploading?: boolean
  allowExternalUrl?: boolean
  optional?: boolean
  adAccountId?: number | null
  onModeChange: (mode: MetaRequestAssetSelectionState["mode"]) => void
  onPatch: (patch: Partial<MetaRequestAssetSelectionState>) => void
  onUpload: (file: File | null) => void
  onMetaSelect: (media: MetaReferenceMediaDto) => void
}) {
  const [pickerOpen, setPickerOpen] = useState(false)
  const [previewOpen, setPreviewOpen] = useState(false)
  const editorMode = selection.mode === "uploaded_asset"
    ? "uploaded_asset"
    : selection.mode === "external_url"
      ? "external_url"
      : selection.metaRefSource === "from_meta"
        ? "from_meta"
        : "manual"

  const options = kind === "video"
    ? [
        { value: "from_meta", label: "From Meta" },
        { value: "manual", label: "Manual" },
        { value: "uploaded_asset", label: "Upload" },
      ]
    : [
        { value: "from_meta", label: "From Meta" },
        { value: "manual", label: "Manual" },
        ...(allowExternalUrl ? [{ value: "external_url", label: "External URL" }] : []),
        { value: "uploaded_asset", label: "Upload" },
      ]

  const preview = getSelectionPreviewSource(selection)

  const handleEditorModeChange = (value: string) => {
    if (value === "uploaded_asset") {
      onPatch(clearMetaReferenceDecoration({
        mode: "uploaded_asset",
        metaRefSource: "manual",
        imageHash: "",
        imageUrl: "",
        videoId: "",
      }))
      return
    }

    if (value === "external_url") {
      onPatch(clearMetaReferenceDecoration({
        mode: "external_url",
        metaRefSource: "manual",
        imageHash: "",
        videoId: "",
      }))
      return
    }

    onPatch(value === "from_meta"
      ? clearMetaReferenceDecoration({ mode: "meta_ref", metaRefSource: "from_meta", imageHash: "", imageUrl: "", videoId: "" })
      : clearMetaReferenceDecoration({ mode: "meta_ref", metaRefSource: "manual" }))
  }

  return (
    <div className="space-y-2 rounded-lg border border-slate-200 bg-slate-50 p-3">
      <div className="flex items-center justify-between gap-3">
        <Label className="text-xs font-medium text-slate-700">{title} {optional ? <span className="text-slate-400 font-normal">(optional)</span> : <span className="text-red-500">*</span>}</Label>
        <Select value={editorMode} onValueChange={handleEditorModeChange}>
          <SelectTrigger className="h-8 w-[160px] text-xs"><SelectValue /></SelectTrigger>
          <SelectContent>{options.map((option) => <SelectItem key={option.value} value={option.value}>{option.label}</SelectItem>)}</SelectContent>
        </Select>
      </div>
      {editorMode === "uploaded_asset" ? (
        <div className="space-y-2">
          <Input type="file" accept={kind === "image" ? "image/*" : "video/*"} className="h-9 text-sm" onChange={(event) => onUpload(event.target.files?.[0] ?? null)} />
          {uploading ? <p className="text-[11px] text-slate-500 flex items-center gap-1"><Loader2 className="w-3 h-3 animate-spin" /> Uploading...</p> : null}
          {selection.uploadedAssetId ? <p className="text-[11px] text-green-700">Stored asset: {selection.uploadedAssetName || `Asset #${selection.uploadedAssetId}`}</p> : null}
        </div>
      ) : editorMode === "external_url" ? (
        <div className="space-y-1.5">
          <Label className="text-[11px] text-slate-500">Image URL</Label>
          <Input value={selection.imageUrl} onChange={(event) => onPatch(clearMetaReferenceDecoration({ imageUrl: event.target.value, metaRefSource: "manual" }))} className="h-9 text-sm" />
        </div>
      ) : editorMode === "manual" ? (
        <div className="space-y-1.5">
          <Label className="text-[11px] text-slate-500">{kind === "video" ? "Meta Video ID" : "Meta Image Hash"}</Label>
          <Input
            value={kind === "video" ? selection.videoId : selection.imageHash}
            onChange={(event) => onPatch(clearMetaReferenceDecoration(kind === "video"
              ? { videoId: event.target.value, metaRefSource: "manual" }
              : { imageHash: event.target.value, metaRefSource: "manual", imageUrl: "" }))}
            className="h-9 text-sm font-mono"
          />
        </div>
      ) : (
        <div className="space-y-3 rounded-md border border-slate-200 bg-white p-3">
          <div className="flex flex-wrap items-center justify-between gap-3">
            <div className="space-y-1">
              <p className="text-xs font-medium text-slate-700">Browse Meta library</p>
              <p className="text-[11px] text-slate-500">Search existing {kind === "video" ? "videos" : "images"} from the selected Meta ad account.</p>
            </div>
            <Button type="button" variant="outline" size="sm" className="h-8 text-xs" onClick={() => setPickerOpen(true)} disabled={!adAccountId}>
              From Meta
            </Button>
          </div>
          {!adAccountId ? (
            <p className="text-[11px] text-amber-600">Select a Meta ad account first to browse media from Meta.</p>
          ) : null}
          {selection.metaAssetId || preview.url ? (
            <div className="flex items-start gap-3 rounded-md border border-slate-200 bg-slate-50 p-3">
              <div className="h-16 w-16 overflow-hidden rounded-md bg-slate-100">
                {preview.url ? (
                  <ProtectedMediaImage
                    src={preview.url}
                    requiresAuth={preview.requiresAuth}
                    alt={selection.metaAssetName || selection.metaAssetId || title}
                    className="h-full w-full object-cover"
                    fallback={<div className="flex h-full items-center justify-center text-slate-400">{kind === "video" ? <Video className="h-5 w-5" /> : <ImageIcon className="h-5 w-5" />}</div>}
                  />
                ) : (
                  <div className="flex h-full items-center justify-center text-slate-400">{kind === "video" ? <Video className="h-5 w-5" /> : <ImageIcon className="h-5 w-5" />}</div>
                )}
              </div>
              <div className="min-w-0 flex-1 space-y-1">
                <p className="truncate text-xs font-medium text-slate-800">{selection.metaAssetName || (kind === "video" ? selection.videoId : selection.imageHash) || "No Meta asset selected"}</p>
                <p className="truncate font-mono text-[11px] text-slate-500">{kind === "video" ? (selection.videoId || selection.metaAssetId || "-") : (selection.imageHash || selection.metaAssetId || "-")}</p>
              </div>
              {kind === "video" ? (
                <Button
                  type="button"
                  variant="outline"
                  size="sm"
                  className="h-8 text-xs"
                  onClick={() => setPreviewOpen(true)}
                  disabled={!selection.metaPlayableUrl && !preview.url}
                >
                  Preview video
                </Button>
              ) : null}
            </div>
          ) : (
            <p className="text-[11px] text-slate-500">No Meta asset selected yet.</p>
          )}
          <MetaMediaPickerDialog
            open={pickerOpen}
            onOpenChange={setPickerOpen}
            adAccountId={adAccountId ?? null}
            targetKind={kind}
            selectedAssetId={selection.metaAssetId || (kind === "video" ? selection.videoId : selection.imageHash) || null}
            onSelect={onMetaSelect}
          />
          {kind === "video" ? (
            <MetaVideoPreviewDialog
              open={previewOpen}
              onOpenChange={setPreviewOpen}
              title={selection.metaAssetName || selection.videoId || title}
              playableUrl={selection.metaPlayableUrl}
              thumbnailUrl={preview.url}
              requiresAuth={preview.requiresAuth}
            />
          ) : null}
        </div>
      )}
    </div>
  )
}

function InlineMediaSourceEditor({
  selection,
  uploading,
  adAccountId,
  onModeChange,
  onPatch,
  onUpload,
  onMetaSelect,
}: {
  selection: MetaRequestAssetSelectionState
  uploading?: boolean
  adAccountId?: number | null
  onModeChange: (mode: MetaRequestAssetSelectionState["mode"]) => void
  onPatch: (patch: Partial<MetaRequestAssetSelectionState>) => void
  onUpload: (file: File | null) => void
  onMetaSelect: (media: MetaReferenceMediaDto) => void
}) {
  const [pickerOpen, setPickerOpen] = useState(false)
  const editorMode = selection.mode === "uploaded_asset"
    ? "uploaded_asset"
    : selection.mode === "external_url"
      ? "external_url"
      : selection.metaRefSource === "from_meta"
        ? "from_meta"
        : "manual"

  const preview = getSelectionPreviewSource(selection)

  const handleEditorModeChange = (value: string) => {
    if (value === "uploaded_asset") {
      onPatch(clearMetaReferenceDecoration({
        mode: "uploaded_asset",
        metaRefSource: "manual",
        imageHash: "",
        imageUrl: "",
      }))
      return
    }

    if (value === "external_url") {
      onPatch(clearMetaReferenceDecoration({ mode: "external_url", metaRefSource: "manual", imageHash: "" }))
      return
    }

    onPatch(value === "from_meta"
      ? clearMetaReferenceDecoration({ mode: "meta_ref", metaRefSource: "from_meta", imageHash: "", imageUrl: "", videoId: "" })
      : clearMetaReferenceDecoration({ mode: "meta_ref", metaRefSource: "manual" }))
  }

  return (
    <div className="space-y-2 rounded-md border border-slate-200 bg-white p-3">
      <div className="flex items-center justify-between gap-3">
        <Label className="text-xs font-medium text-slate-700">Card Image <span className="text-red-500">*</span></Label>
        <Select value={editorMode} onValueChange={handleEditorModeChange}>
          <SelectTrigger className="h-8 w-[160px] text-xs"><SelectValue /></SelectTrigger>
          <SelectContent>
            <SelectItem value="from_meta">From Meta</SelectItem>
            <SelectItem value="manual">Manual</SelectItem>
            <SelectItem value="external_url">External URL</SelectItem>
            <SelectItem value="uploaded_asset">Upload</SelectItem>
          </SelectContent>
        </Select>
      </div>
      {editorMode === "uploaded_asset" ? (
        <div className="space-y-2">
          <Input type="file" accept="image/*" className="h-9 text-sm" onChange={(event) => onUpload(event.target.files?.[0] ?? null)} />
          {uploading ? <p className="text-[11px] text-slate-500 flex items-center gap-1"><Loader2 className="w-3 h-3 animate-spin" /> Uploading...</p> : null}
          {selection.uploadedAssetId ? <p className="text-[11px] text-green-700">Stored asset: {selection.uploadedAssetName || `Asset #${selection.uploadedAssetId}`}</p> : null}
        </div>
      ) : editorMode === "external_url" ? (
        <Input value={selection.imageUrl} onChange={(event) => onPatch(clearMetaReferenceDecoration({ imageUrl: event.target.value, metaRefSource: "manual" }))} className="h-9 text-sm" />
      ) : editorMode === "manual" ? (
        <Input value={selection.imageHash} onChange={(event) => onPatch(clearMetaReferenceDecoration({ imageHash: event.target.value, metaRefSource: "manual", imageUrl: "" }))} className="h-9 text-sm font-mono" />
      ) : (
        <div className="space-y-3 rounded-md border border-slate-200 bg-slate-50 p-3">
          <div className="flex flex-wrap items-center justify-between gap-3">
            <p className="text-[11px] text-slate-500">Pick an image from Meta for this carousel card.</p>
            <Button type="button" variant="outline" size="sm" className="h-8 text-xs" onClick={() => setPickerOpen(true)} disabled={!adAccountId}>From Meta</Button>
          </div>
          {preview.url ? (
            <div className="flex items-center gap-3 rounded-md border border-slate-200 bg-white p-2">
              <div className="h-14 w-14 overflow-hidden rounded-md bg-slate-100">
                <ProtectedMediaImage src={preview.url} requiresAuth={preview.requiresAuth} alt={selection.metaAssetName || selection.imageHash || "Card image"} className="h-full w-full object-cover" fallback={<div className="flex h-full items-center justify-center text-slate-400"><ImageIcon className="h-4 w-4" /></div>} />
              </div>
              <div className="min-w-0 flex-1">
                <p className="truncate text-xs font-medium text-slate-800">{selection.metaAssetName || selection.imageHash || "Meta image selected"}</p>
                <p className="truncate font-mono text-[11px] text-slate-500">{selection.imageHash || selection.metaAssetId || "-"}</p>
              </div>
            </div>
          ) : (
            <p className="text-[11px] text-slate-500">No Meta image selected yet.</p>
          )}
          <MetaMediaPickerDialog
            open={pickerOpen}
            onOpenChange={setPickerOpen}
            adAccountId={adAccountId ?? null}
            targetKind="image"
            selectedAssetId={selection.metaAssetId || selection.imageHash || null}
            onSelect={onMetaSelect}
          />
        </div>
      )}
    </div>
  )
}

function TextVariationEditor({
  label,
  values,
  required,
  multiline,
  placeholder,
  onChange,
}: {
  label: string
  values: string[]
  required?: boolean
  multiline?: boolean
  placeholder?: string
  onChange: (values: string[]) => void
}) {
  const rows = normalizeVariationRows(values)

  return (
    <div className="space-y-2 rounded-lg border border-slate-200 bg-slate-50 p-3">
      <div className="flex items-center justify-between gap-3">
        <div>
          <Label className="text-xs font-medium text-slate-700">{label} {required ? <span className="text-red-500">*</span> : null}</Label>
          <p className="text-[11px] text-slate-400">Add up to {maxCreativeTextVariations} variations. Meta can rotate these variations at delivery time.</p>
        </div>
        <Button
          type="button"
          variant="outline"
          size="sm"
          className="h-8 text-xs"
          onClick={() => onChange([...rows, ""])}
          disabled={rows.length >= maxCreativeTextVariations}
        >
          <Plus className="mr-1 h-3.5 w-3.5" />Add text
        </Button>
      </div>
      <div className="space-y-2">
        {rows.map((value, index) => (
          <div key={`${label}-${index}`} className="flex items-start gap-2">
            <div className="min-w-0 flex-1">
              {multiline ? (
                <Textarea
                  rows={3}
                  value={value}
                  placeholder={placeholder || `${label} ${index + 1}`}
                  onChange={(event) => {
                    const nextValues = [...rows]
                    nextValues[index] = event.target.value
                    onChange(nextValues)
                  }}
                  className="text-sm resize-none bg-white"
                />
              ) : (
                <Input
                  value={value}
                  placeholder={placeholder || `${label} ${index + 1}`}
                  onChange={(event) => {
                    const nextValues = [...rows]
                    nextValues[index] = event.target.value
                    onChange(nextValues)
                  }}
                  className="h-9 text-sm bg-white"
                />
              )}
            </div>
            <Button
              type="button"
              variant="ghost"
              size="sm"
              className="mt-1 h-8 px-2 text-red-600"
              onClick={() => onChange(rows.filter((_, rowIndex) => rowIndex !== index))}
              disabled={rows.length <= 1}
            >
              <Trash2 className="h-3.5 w-3.5" />
            </Button>
          </div>
        ))}
      </div>
    </div>
  )
}

function getCreativeCompletion(form: RequestFormState) {
  if (form.creativeType === "SINGLE_VIDEO") {
    const items = [
      { label: "Creative name", ok: !!form.creativeName },
      { label: "Facebook Page ID", ok: !!form.facebookPageId },
      { label: "Message", ok: hasAnyTextVariation(form.singleVideoPrimaryTexts, form.singleVideoPrimaryText) },
      { label: "Headline", ok: hasAnyTextVariation(form.singleVideoHeadlines, form.singleVideoHeadline) },
      { label: "CTA", ok: !!form.singleVideoCallToAction },
      { label: "Video source", ok: !!(form.singleVideoVideo.uploadedAssetId || form.singleVideoVideo.videoId) },
    ]
    return { complete: items.every((item) => item.ok), items }
  }
  if (form.creativeType === "CAROUSEL_IMAGE") {
    const items = [
      { label: "Creative name", ok: !!form.creativeName },
      { label: "Facebook Page ID", ok: !!form.facebookPageId },
      { label: "CTA", ok: !!form.carouselCallToAction },
      { label: "At least 2 cards", ok: form.carouselCards.length >= 2 },
      { label: "Cards complete", ok: form.carouselCards.every((card) => !!card.headline && !!(card.image.uploadedAssetId || card.image.imageHash || card.image.imageUrl)) },
    ]
    return { complete: items.every((item) => item.ok), items }
  }
  if (form.creativeType === "FLEXIBLE") {
    const items = [
      { label: "Creative name", ok: !!form.creativeName },
      { label: "Facebook Page ID", ok: !!form.facebookPageId },
      { label: "Primary text", ok: hasAnyTextVariation(form.flexiblePrimaryTexts) },
      { label: "Headline", ok: hasAnyTextVariation(form.flexibleHeadlines) },
      { label: "CTA", ok: !!form.flexibleCallToAction },
      { label: "Link URL or app fallback", ok: true },
      { label: "At least 1 asset", ok: form.flexibleAssets.length >= 1 },
      {
        label: "Assets complete",
        ok: form.flexibleAssets.length > 0 && form.flexibleAssets.every((asset) => asset.assetType === "VIDEO"
          ? !!(asset.video.uploadedAssetId || asset.video.videoId)
          : !!(asset.image.uploadedAssetId || asset.image.imageHash || asset.image.imageUrl)),
      },
    ]
    return { complete: items.every((item) => item.ok), items }
  }
  if (form.creativeType === "EXISTING_POST") {
    const items = [
      { label: "Creative name", ok: !!form.creativeName },
      { label: "Facebook Page ID", ok: !!form.facebookPageId },
      { label: "Source Post ID", ok: !!form.existingPostId },
    ]
    return { complete: items.every((item) => item.ok), items }
  }
  const items = [
    { label: "Creative name", ok: !!form.creativeName },
    { label: "Facebook Page ID", ok: !!form.facebookPageId },
    { label: "Message", ok: hasAnyTextVariation(form.singleImagePrimaryTexts, form.singleImagePrimaryText) },
    { label: "Headline", ok: hasAnyTextVariation(form.singleImageHeadlines, form.singleImageHeadline) },
    { label: "CTA", ok: !!form.singleImageCallToAction },
    { label: "Image source", ok: !!(form.singleImageImage.uploadedAssetId || form.singleImageImage.imageHash || form.singleImageImage.imageUrl) },
  ]
  return { complete: items.every((item) => item.ok), items }
}

function normalizeVariationRows(values?: string[]): string[] {
  const nextValues = Array.isArray(values) ? values.slice(0, maxCreativeTextVariations) : []
  return nextValues.length > 0 ? nextValues : [""]
}

function getFirstFilledVariation(values?: string[], fallback = ""): string {
  const found = normalizeVariationRows(values)
    .map((value) => value.trim())
    .find((value) => value.length > 0)
  return found || fallback
}

function hasAnyTextVariation(values?: string[], fallback = ""): boolean {
  return !!getFirstFilledVariation(values, fallback)
}

function getPreviewImage(form: RequestFormState): { url: string; requiresAuth: boolean } {
  if (form.creativeType === "SINGLE_VIDEO") return getSelectionPreviewSource(form.singleVideoThumbnail)
  if (form.creativeType === "CAROUSEL_IMAGE") return getSelectionPreviewSource(form.carouselCards[0]?.image)
  if (form.creativeType === "FLEXIBLE") {
    const firstAsset = form.flexibleAssets[0]
    if (!firstAsset) return { url: "", requiresAuth: false }
    return firstAsset.assetType === "VIDEO" ? getSelectionPreviewSource(firstAsset.thumbnail) : getSelectionPreviewSource(firstAsset.image)
  }
  return getSelectionPreviewSource(form.singleImageImage)
}

function getPreviewHeadline(form: RequestFormState): string {
  if (form.creativeType === "SINGLE_VIDEO") return getFirstFilledVariation(form.singleVideoHeadlines, form.singleVideoHeadline)
  if (form.creativeType === "CAROUSEL_IMAGE") return form.carouselCards[0]?.headline || ""
  if (form.creativeType === "FLEXIBLE") return getFirstFilledVariation(form.flexibleHeadlines)
  if (form.creativeType === "EXISTING_POST") return form.existingPostId ? `Existing Post ${form.existingPostId}` : "Existing post preview"
  return getFirstFilledVariation(form.singleImageHeadlines, form.singleImageHeadline)
}

function getPreviewMessage(form: RequestFormState): string {
  if (form.creativeType === "SINGLE_VIDEO") return getFirstFilledVariation(form.singleVideoPrimaryTexts, form.singleVideoPrimaryText)
  if (form.creativeType === "CAROUSEL_IMAGE") return form.carouselPrimaryText || `${form.carouselCards.length} carousel cards`
  if (form.creativeType === "FLEXIBLE") return getFirstFilledVariation(form.flexiblePrimaryTexts) || `${form.flexibleAssets.length} flexible assets`
  if (form.creativeType === "EXISTING_POST") return "Existing post preview will be resolved from Meta post at execution time."
  return getFirstFilledVariation(form.singleImagePrimaryTexts, form.singleImagePrimaryText)
}

function getPreviewCta(form: RequestFormState): string {
  if (form.creativeType === "SINGLE_VIDEO") return formatCta(form.singleVideoCallToAction)
  if (form.creativeType === "CAROUSEL_IMAGE") return formatCta(form.carouselCallToAction)
  if (form.creativeType === "FLEXIBLE") return formatCta(form.flexibleCallToAction)
  if (form.creativeType === "EXISTING_POST") return "OPEN POST"
  return formatCta(form.singleImageCallToAction)
}

function formatCta(value: string): string {
  return value ? value.replaceAll("_", " ") : "LEARN MORE"
}























