function buildMetaLibrarySelectionPatch(media: MetaReferenceMediaDto, adAccountId?: number | null): Partial<MetaRequestAssetSelectionState> {
  return {
    mode: "meta_ref",
    metaRefSource: "from_meta",
    imageHash: media.assetType === "IMAGE" ? (media.hash ?? "") : "",
    imageUrl: "",
    videoId: media.assetType === "VIDEO" ? (media.videoId ?? media.id) : "",
    uploadedAssetId: null,
    uploadedAssetName: "",
    uploadedAssetPreviewUrl: "",
    metaPreviewUrl: media.thumbnailUrl ?? "",
    metaPreviewRequiresAuth: !!media.requiresAuth,
    metaPlayableUrl: media.playableUrl ?? "",
    metaAssetId: media.id,
    metaAssetName: media.name ?? "",
    metaAssetType: media.assetType,
    metaAdAccountId: adAccountId != null ? String(adAccountId) : "",
  }
}

function clearMetaReferenceDecoration(patch?: Partial<MetaRequestAssetSelectionState>): Partial<MetaRequestAssetSelectionState> {
  return {
    uploadedAssetId: null,
    uploadedAssetName: "",
    uploadedAssetPreviewUrl: "",
    ...patch,
    metaPreviewUrl: "",
    metaPreviewRequiresAuth: false,
    metaPlayableUrl: "",
    metaAssetId: "",
    metaAssetName: "",
    metaAssetType: "",
    metaAdAccountId: "",
  }
}
