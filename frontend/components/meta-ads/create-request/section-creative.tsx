function buildMetaLibrarySelectionPatch(media: MetaReferenceMediaDto, adAccountId?: number | null): Partial<MetaRequestAssetSelectionState> {
  return {
    mode: "meta_ref",
    metaRefSource: "from_meta",
    imageHash: media.assetType === "IMAGE" ? (media.hash ?? "") : "",
    imageUrl: "",
    videoId: media.assetType === "VIDEO" ? (media.videoId ?? media.id) : "",
    uploadedAssetId: null,
    uploadedAssetName: "",
    uploadedAssetPreviewUrl: "",
    metaPreviewUrl: media.thumbnailUrl ?? "",
    metaPreviewRequiresAuth: !!media.requiresAuth,
    metaPlayableUrl: media.playableUrl ?? "",
    metaAssetId: media.id,
    metaAssetName: media.name ?? "",
    metaAssetType: media.assetType,
    metaAdAccountId: adAccountId != null ? String(adAccountId) : "",
  }
}

function getFrameCaptureVideoUrl(selection: MetaRequestAssetSelectionState): string {
  return selection.metaPlayableUrl
}

function getFrameCaptureVideoUrlResolver(selection: MetaRequestAssetSelectionState): (() => Promise<string | null>) | undefined {
  if (selection.metaRefSource === "from_meta" && selection.metaAdAccountId && selection.videoId) {
    const adAccountId = Number(selection.metaAdAccountId)
    if (Number.isFinite(adAccountId)) {
      return async () => {
        const result = await metaReferenceApi.getAdAccountVideoCaptureUrl(adAccountId, selection.videoId)
        return result.url
      }
    }
  }

  return undefined
}

function clearMetaReferenceDecoration(patch?: Partial<MetaRequestAssetSelectionState>): Partial<MetaRequestAssetSelectionState> {
  return {
    uploadedAssetId: null,
    uploadedAssetName: "",
    uploadedAssetPreviewUrl: "",
    ...patch,
    metaPreviewUrl: "",
    metaPreviewRequiresAuth: false,
    metaPlayableUrl: "",
    metaAssetId: "",
    metaAssetName: "",
    metaAssetType: "",
    metaAdAccountId: "",
  }
}
