function toggleValue(values: string[], value: string, checked: boolean) {
  return checked ? Array.from(new Set([...values, value])) : values.filter((item) => item !== value)
}

function OptionChecks({ options, values, onValuesChange }: { options: TikTokOptionDto[]; values: string[]; onValuesChange: (values: string[]) => void }) {
  return (
    <div className="grid gap-2 sm:grid-cols-2">
      {options.map((option) => (
        <label key={option.key} className="flex min-h-10 items-center gap-2 rounded-md border bg-background px-3 py-2 text-sm">
          <Checkbox checked={values.includes(option.key)} onCheckedChange={(checked) => onValuesChange(toggleValue(values, option.key, checked === true))} />
          <span className="min-w-0 truncate">{optionLabel(option)}</span>
        </label>
      ))}
    </div>
  )
}

interface LocationNode {
  option: TikTokTargetingOptionDto
  children: LocationNode[]
}

function sortLocationNodes(nodes: LocationNode[]) {
  nodes.sort((first, second) => first.option.label.localeCompare(second.option.label) || first.option.key.localeCompare(second.option.key))
  nodes.forEach((node) => sortLocationNodes(node.children))
}

function buildLocationTree(options: TikTokTargetingOptionDto[]) {
  const nodes = new Map<string, LocationNode>()
  options.forEach((option) => nodes.set(option.key, { option, children: [] }))

  const roots: LocationNode[] = []
  nodes.forEach((node) => {
    const parentId = node.option.parentId?.trim()
    const parent = parentId && parentId !== node.option.key ? nodes.get(parentId) : undefined
    if (parent) parent.children.push(node)
    else roots.push(node)
  })

  sortLocationNodes(roots)
  return roots
}

function locationSearchText(option: TikTokTargetingOptionDto) {
  return `${option.label} ${option.key} ${option.countryCode ?? ""} ${option.level ?? ""} ${option.path ?? ""}`.toLowerCase()
}

function filterLocationTree(nodes: LocationNode[], query: string): LocationNode[] {
  const term = query.trim().toLowerCase()
  if (!term) return nodes

  return nodes.flatMap((node) => {
    const filteredChildren = filterLocationTree(node.children, term)
    if (locationSearchText(node.option).includes(term) || filteredChildren.length > 0) {
      return [{ ...node, children: filteredChildren }]
    }
    return []
  })
}

function isCountryNode(option: TikTokTargetingOptionDto) {
  const level = option.level?.toUpperCase() ?? ""
  return level.includes("COUNTRY") || !option.parentId
}

